function getObj(num){
    if ((num > 999)||(num < 0)){
        
        console.log("Введено неверное число");
        return null;}
    var obj = new Object();
    obj['единицы'] = num%10;
    obj['десятки'] = Math.floor((num%100)/10);
    obj['сотни'] = (num - num%100)/100;
    return(obj);
  }

