function isAnswer(q, event) {
    //Восклицательный знак возвращает нам противоположное булевое значение
    //!true===false
    if (isNaN(event) || !isFinite(event)) { // Посмотрите в интернете про эти функции, если кто не знает их)
        alert('Введен недопустимый символ');
        return false;
    } else if (event < 1 || event > q) {
        alert('Ваше число выходит из диапозона');
        return false
    }
    return true;
}